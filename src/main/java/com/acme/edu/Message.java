package com.acme.edu;

/**
 * Print message
 */
public class Message {

    public static void systemOut(Object s) {
        System.out.println(s);
    }

    public static void systemOut(String s) {
        System.out.println(s);
    }

    public static void systemPrint(Object s) {
        System.out.print(s);
    }
}
